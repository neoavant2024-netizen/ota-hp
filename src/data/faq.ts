/** 構造化データ用にすべての Q&A を平坦化 */
export const allFaqs = faqGroups.flatMap((g) => g.items);

export const faqToText = (f: Faq): string =>
  [f.a, ...(f.list ?? []).map((x, i) => `${i + 1}. ${x}`), ...(f.sub ?? []).map((s) => `${s.t} ${s.d}`), f.note]
    .filter(Boolean)
    .join(' ');
